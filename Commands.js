        function getRandomText() {
            const totalWeight = textSets.reduce((sum, item) => sum + item.weight, 0);
            const random = Math.random() * totalWeight;

            let current = 0;

            for (const item of textSets) {
                current += item.weight;
                if (random < current) {
                    return item.text;
                }
            }
        }

