        let textIndex = 0;
        let charIndex = 0;

        const typeElement = document.getElementById("typewriter");
        const button = document.querySelector(".center-screen button");

        button.style.opacity = "0";
        button.style.pointerEvents = "none";

        function typeWriter() {
            if (charIndex < texts[textIndex].length) {
                typeElement.innerHTML += texts[textIndex].charAt(charIndex);
                charIndex++;
                setTimeout(typeWriter, 60);
            } else {
                setTimeout(eraseText, 1200);
            }
        }

        function eraseText() {
            if (charIndex > 0) {
                typeElement.innerHTML = texts[textIndex].substring(0, charIndex - 1);
                charIndex--;
                setTimeout(eraseText, 30);
            } else {
                textIndex++;
                if (textIndex < texts.length) {
                    setTimeout(typeWriter, 400);
                } else {
                    showButton();
                }
            }
        }

        function showButton() {
            button.style.transition = "opacity 1.5s ease";
            button.style.opacity = "1";
            button.style.pointerEvents = "auto";
        }

